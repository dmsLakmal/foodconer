    private void updateTFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateTFActionPerformed
        // TODO add your handling code here:
        String catogary = catCMB1.getSelectedItem().toString();
        UpdateController.updateProduct(productIdTF1.getText(),nameTF1.getText(),quanTF1.getText(),priceTF1.getText(),catogary);

        ResultSet r = new DBSearch().searchProduct();
        DefaultTableModel dtm = (DefaultTableModel) addProductTbl.getModel();
        dtm.setRowCount(0); //set the number of rows in the model
        try{
            Vector v;
            while (r.next()){
                v = new Vector();
                v.add(r.getString("ProductID"));
                v.add(r.getString("Name"));
                v.add(r.getString("Quantity"));
                v.add(r.getString("Price"));
                v.add(r.getString("category"));
                dtm.addRow(v); //adds a row to the end of the table
            }
            DBConnection.closeCon();

        }
        catch(Exception e)
        {

        }

    }//GEN-LAST:event_updateTFActionPerformed

