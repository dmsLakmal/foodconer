    private void addProductTblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addProductTblMouseClicked
        // TODO add your handling code here:
        int i = addProductTbl.getSelectedRow();
        TableModel model = addProductTbl.getModel();
        productIdTF1.setText(model.getValueAt(i,0).toString());
        priceTF1.setText(model.getValueAt(i,3).toString());
        nameTF1.setText(model.getValueAt(i,1).toString());
        quanTF1.setText(model.getValueAt(i,2).toString());
        


        
        String catogary = model.getValueAt(i,4).toString();
        switch(catogary){
            case "Bevarage":
                catCMB1.setSelectedIndex(1);
                break;

            case "Meat":
                catCMB1.setSelectedIndex(2);
                break;  
                
            case "Vegitable":
                catCMB1.setSelectedIndex(3);
                break;  
            
        }
        
    }//GEN-LAST:event_addProductTblMouseClicked

    private void searchTxtKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchTxtKeyReleased
        // TODO add your handling code here:
        DefaultTableModel table;
        table = (DefaultTableModel) addProductTbl.getModel();
        String search = searchTxt.getText().toLowerCase();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<>(table);
        addProductTbl.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(search));
 
    }//GEN-LAST:event_searchTxtKeyReleased

